export default function TabNavigation() {
  const pathname = usePathname();

  const isActive = (href: string) => {
    if (href === '/') return pathname === '/';
    return pathname.startsWith(href);
  };

  return (
    <div className="sticky top-16 z-40 bg-white/80 backdrop-blur-md border-b border-gray-100">
      <div className="max-w-6xl mx-auto px-4">
        <nav className="flex items-center justify-center gap-1 py-2">
          {tabs.map((tab) => (
            <Link
              key={tab.name}
              href={tab.href}
              className={`relative flex items-center gap-2 px-4 py-2.5 rounded-xl font-medium transition-all duration-200 ${
                isActive(tab.href)
                  ? 'bg-gradient-to-r from-eco-green to-emerald-500 text-white shadow-md shadow-eco-green/20'
                  : 'text-gray-600 hover:bg-gray-100 hover:text-gray-900'
              }`}
            >
              {tab.icon}
              <span className="hidden sm:inline">{tab.name}</span>
              {isActive(tab.href) && (
                <span className="absolute -bottom-2 left-1/2 -translate-x-1/2 w-1 h-1 bg-eco-green rounded-full" />
              )}
            </Link>
          ))}
        </nav>
      </div>
    </div>
  );
}
